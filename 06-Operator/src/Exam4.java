
public class Exam4 {
	public static void main(String[] args) {
		int num1 =1 ;
		System.out.println("num1= "+ num1);
		
		//변수값 1증가 
		
		num1 =num1 +1 ;
		System.out.println("num1= "+ num1);
		System.out.println("-------------");
		
		num1 +=1;
		System.out.println("num1= "+ num1);
		System.out.println("-------------");
		
		num1 ++;
		System.out.println("num1= "+ num1);
		System.out.println("-------------");
		
		++num1;
		System.out.println("num1= "+ num1);
		System.out.println("-------------");
		
		//변수값 1감소 
		num1= num1 -1 ;
		System.out.println("num1= "+ num1);
		System.out.println("-------------");
		
		num1 -=1 ;
		System.out.println("num1= "+ num1);
		System.out.println("-------------");
		
		
		
		
	}

}
