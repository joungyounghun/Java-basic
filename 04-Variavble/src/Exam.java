
public class Exam{
	public static void main(String[] args) {
		// 정수
		int num1;		// -20억 ~ +20억
		num1 = 5;
		System.out.println("num1 = " + num1);
		System.out.println("-----------------");
		
		int num2 = 7;
		System.out.println("num2 = " + num2);
		System.out.println("-----------------");
		
		long num3 = 1234567890123456789L;  // -800경 ~ +800경
		System.out.println("num3 = " + num3);
		System.out.println("-----------------");
		
		byte num4 = 5;  // -128 ~ 127
		System.out.println("num4 = " + num4);
		System.out.println("-----------------");
		
		short num5 = 7;	// -3만 ~ +3만
		System.out.println("num5 = " + num5);
		System.out.println("-----------------");
		
		// 실수
		double num6 = 5.5;
		System.out.println("num6 = " + num6);
		System.out.println("-----------------");
		
		float num7 = 5.5f;
		System.out.println("num7 = " + num7);
		System.out.println("-----------------");
		
		// 문자
		char ch1 = 'A';
		System.out.println("ch1 = " + ch1);
		System.out.println("-----------------");
		
		char ch2 = '홍';
		System.out.println("ch2 = " + ch2);
		System.out.println("-----------------");
		
		ch2 = '김';
		System.out.println("ch2 = " + ch2);
		System.out.println("-----------------");
		
		// boolean값
		boolean b1 = true;
		System.out.println("b1 = " + b1);
		System.out.println("-----------------");
		
		boolean b2 = false;
		System.out.println("b2 = " + b2);
		System.out.println("-----------------");
	}
}